fn turn_bit_num_to_time_string(time_num: u16) -> String {
    let minute_num = (time_num & 0b111111) as u8;
    let hour_num = ((time_num & 0b1111000000) >> 6) as u8;

    fn digit_to_digit_char(n: u8) -> char {
        (b'0' + n) as char
    }

    let mut ans_string = String::with_capacity(5);

    if (hour_num / 10) > 0 {
        ans_string.push(digit_to_digit_char(hour_num / 10));
    }
    ans_string.push(digit_to_digit_char(hour_num % 10));
    ans_string.push(':');
    ans_string.push(digit_to_digit_char(minute_num / 10));
    ans_string.push(digit_to_digit_char(minute_num % 10));

    ans_string
}

